//  >  Greater
//  <  Less
// ==  Equal
// !=  Not Equal
// >=  Greater than
// <=  Less than


var MAR = ("Marvel");
var DC = ("DC");

if (MAR == DC) {
   console.log(" Marvel is as strong as DC");
} else if (MAR > DC) {
  console.log(" Marvel is stronger than DC");
} else if (MAR < DC) { 
  console.log("Marvel is weaker than DC");
} else {
  console.log(" Marvel is different than DC");
}
